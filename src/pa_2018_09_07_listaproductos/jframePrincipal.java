    private void jbutcalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbutcalcularActionPerformed
        // TODO add your handling code here:
        Producto equis = new Producto();
        
        DecimalFormat f = new DecimalFormat("###,###.00");
        double total = 0;
        for(int r = 0; r < jTableProductos.getRowCount();r++)
        {
            equis.setDescripcion(jTableProductos.getValueAt(r, 0)+"");
            equis.setCosto(Double.parseDouble(jTableProductos.getValueAt(r, 1)+""));
            equis.setMargenUtilidad(Double.parseDouble(jTableProductos.getValueAt(r, 2)+""));
            equis.setPorcentajeDescuento(Double.parseDouble(jTableProductos.getValueAt(r, 3)+""));
            equis.setPorcentajeImpuesto(Double.parseDouble(jTableProductos.getValueAt(r, 4)+""));
            
            jTableProductos.setValueAt(f.format(equis.importeUtilidad()),r,5);
            jTableProductos.setValueAt(f.format(equis.importeDescuento()),r,6);
            jTableProductos.setValueAt(f.format(equis.importeImpuesto()),r,7);
            jTableProductos.setValueAt(f.format(equis.precio()),r,8);
            
            total += equis.precio();
        }
        
        double promPrecio = total / jTableProductos.getRowCount();
        jLabPromedio.setText(f.format(promPrecio));
    }//GEN-LAST:event_jbutcalcularActionPerformed

